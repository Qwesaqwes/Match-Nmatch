int		match(char *s1, char *s2)
{
	if (*s1 == *s2)
	{
		if (*s1 == '\0')
			return (1);
		return (match(s1 + 1, s2 + 1));
	}
	else
	{
		if (*s2 == '*')
		{
			if (*(s2 + 1) == '*')
				return (match(s1, s2 + 1));
			if (*s1 != *(s2 + 1))
				return (match(s1 + 1, s2));
			else
			{
				if (match(s1, s2 + 1) == 0)
					return (match(s1 + 1, s2));
				else
					return (1);
			}
		}
		return (0);
	}
}
